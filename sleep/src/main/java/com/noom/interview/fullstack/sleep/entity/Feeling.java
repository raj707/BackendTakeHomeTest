package com.noom.interview.fullstack.sleep.entity;

/**
 * Represents how the user felt after waking up from a night's sleep.
 */
public enum Feeling {

    /**
     * User had a poor sleep experience.
     */
    BAD,

    /**
     * User had an average sleep experience.
     */
    OK,

    /**
     * User had a good sleep experience.
     */
    GOOD
}
